/**
 * 搜索
 * @params {string} key
 * @returns {[{title, summary, cover, url}]}
 */
function search(key) {
	var url = `https://api.upyunso.com/search?keyword=${encodeURI(key)}` + header;
	var response = httpRequest(url);
	response = Base64.decode(response);//解码
	var array= [];
	const $ = JSON.parse(response)
	$.result.items.forEach((child) => {
		var page_url = child.page_url;
		if(page_url.indexOf('www.sqxbs.com') == -1){
			array.push({
				//标题
				title: child.title,
		
				//概览
				summary: child.insert_time + '\n' + child.content.length + '个文件',
		
				//封面
				cover: '',
		
				//网址
				url: ToolUtil.urlJoin(url,page_url)
			})
		}
	  })
	return JSON.stringify(array);
}

